class ChatThread {
  final String id;
  final String name;
  final String subtitle;
  final String lastTime;
  final String status;
  final String avatarUrl;

  ChatThread({
    required this.id,
    required this.name,
    required this.subtitle,
    required this.lastTime,
    required this.status,
    required this.avatarUrl,
  });
}
